package javaFainal.operator.conditionalStatemen_조건문;

public class MathBaisc_수학기본 {
	public static void main(String[] args) {

		int aka = 15;
		int ao = 3;

		System.out.println(aka + ao); // 덧셈
		System.out.println(aka - ao); // 빼기
		System.out.println(aka * ao); // 곱하기
		System.out.println(aka / ao); // 나누기
		System.out.println(aka % ao); // 나머지

		System.out.println(Math.max(10, 30)); // 최대
		System.out.println(Math.min(10, 30)); // 최소
		System.out.println(Math.abs(-30)); // 절대값
	}
}
